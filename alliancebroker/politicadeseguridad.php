<b>Alliance Broker</b> es una empresa dedicada a la prestación de servicios legales y de seguros. En esta política de seguridad se describen las medidas que tomamos para proteger la información personal de nuestros clientes y posibles clientes.

<b>Protección de la información personal</b>

Tomamos medidas de seguridad para proteger la información personal de nuestros clientes y posibles clientes del acceso no autorizado, la divulgación, la alteración o la destrucción. Estas medidas incluyen:

Acceso restringido: solo el personal autorizado tiene acceso a la información personal de nuestros clientes y posibles clientes.
Seguridad física: nuestros servidores se encuentran en centros de datos seguros.
Seguridad de la información: utilizamos medidas de seguridad de la información, como cifrado, para proteger la información personal de nuestros clientes y posibles clientes.
Seguridad de los datos

Cuando recopilamos información personal de nuestros clientes y posibles clientes, utilizamos cifrado para protegerla durante la transmisión. También almacenamos la información personal de nuestros clientes y posibles clientes en servidores seguros.

<b>Seguridad de las contraseñas</b>

Los clientes y posibles clientes deben crear contraseñas seguras para sus cuentas en nuestro sitio web. Las contraseñas deben ser al menos de 8 caracteres de longitud e incluir una combinación de letras, números y símbolos.

<b>Seguridad de los formularios web</b>

Todos los formularios web en nuestro sitio web están protegidos con medidas de seguridad para evitar la entrada de datos maliciosos.

<b>Seguridad de los pagos</b>

Para procesar pagos, utilizamos proveedores de servicios de pago externos que cumplen con los estándares de seguridad de la industria.

<b>Avisos de seguridad</b>

Informaremos a nuestros clientes y posibles clientes de cualquier violación de seguridad que pueda afectar su información personal.

<b>Cambios en esta política de seguridad</b>

Podemos modificar esta política de seguridad de vez en cuando. Publicaremos cualquier cambio en esta política de seguridad en nuestro sitio web.

<b>Contacto</b>

Si tiene alguna pregunta o inquietud sobre esta política de seguridad, puede ponerse en contacto con nosotros por correo electrónico a [correo electrónico] o por correo postal a [dirección].

<b>Medidas adicionales de seguridad</b>

Además de las medidas de seguridad descritas anteriormente, tomamos las siguientes medidas adicionales para proteger la información personal de nuestros clientes y posibles clientes:

Formación del personal: nuestro personal está formado en las políticas y procedimientos de seguridad de la información.
Revisiones de seguridad: realizamos revisiones periódicas de nuestras medidas de seguridad para garantizar que sean efectivas.
Investigación de incidentes: investigamos todos los incidentes de seguridad que puedan afectar la información personal de nuestros clientes y posibles clientes.
Responsabilidad

Nos comprometemos a proteger la información personal de nuestros clientes y posibles clientes. Si tiene alguna pregunta o inquietud sobre la seguridad de su información, no dude en ponerse en contacto con nosotros.

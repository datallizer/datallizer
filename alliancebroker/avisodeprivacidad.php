Agentes de seguros: Podemos compartir su información personal con agentes de seguros para que puedan proporcionarle cotizaciones o asesoramiento sobre seguros.
Proveedores de servicios: Podemos compartir su información personal con proveedores de servicios que nos ayudan a proporcionar nuestros servicios, por ejemplo, para procesar pagos o brindar soporte al cliente.
Autoridades gubernamentales: Podemos compartir su información personal con autoridades gubernamentales si así lo exige la ley.

<b>Sus derechos</b>

Usted tiene los siguientes derechos con respecto a su información personal:

Derecho de acceso: Tiene derecho a acceder a su información personal que tenemos en nuestro poder.
Derecho de rectificación: Tiene derecho a solicitar que corrijamos cualquier información personal inexacta que tengamos en nuestro poder.
Derecho de supresión: Tiene derecho a solicitar que eliminemos su información personal de nuestro sistema.
Derecho de portabilidad: Tiene derecho a solicitar que le proporcionemos una copia de su información personal en un formato legible por máquina.
Derecho de oposición: Tiene derecho a oponerse al tratamiento de su información personal.


<b>Alliance Broker</b> es una empresa dedicada a la prestación de servicios legales y de seguros. En este aviso de privacidad se describe cómo recopilamos, utilizamos y compartimos su información personal.

<b>Información que recopilamos</b>

Podemos recopilar la siguiente información personal de usted:

Información de contacto: nombre, apellidos, correo electrónico, teléfono, domicilio.
Información de servicios: servicios de su interés, edad, polizas.
Información de identificación: fotografía de su rostro, ine.

<b>Cómo recopilamos su información</b>

Podemos recopilar su información personal de las siguientes maneras:

Cuando nos la proporciona: Recopilamos información personal cuando nos la proporciona directamente, por ejemplo, cuando se registra en nuestro sitio web, se suscribe a nuestro boletín informativo o solicita un servicio.
Cuando utiliza nuestro sitio web: Recopilamos información personal cuando utiliza nuestro sitio web, por ejemplo, cuando navega por el sitio web, utiliza nuestros formularios o interactúa con nuestras funciones.
De terceros: Podemos recopilar información personal de terceros, por ejemplo, cuando un agente de seguros nos proporciona información sobre usted.

